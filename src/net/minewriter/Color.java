package net.minewriter;

public class Color {

	private final static String P = "\u001B";
	public final static String RESET = P + "[0m";
	public final static String RED = P + "[31m";
	public final static String GREEN = P + "[32m";
	public final static String YELLOW = P + "[33m";
	public final static String BLUE = P + "[34m";
	public final static String MAGENTA = P + "[35m";
	public final static String CYAN = P + "[36m";
}
